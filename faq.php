<strong>Pat has albums?</strong><br>
Yes, and they're far too numerous to list here. Instead, go to <a href="songlist.php">this page</a> which lists them all and has all the lyrics, too. They are all available at our <a href="shop.php">shop</a>, as well as at the shows.  Some are even available on <a href="http://itunes.apple.com/us/artist/pat-mccurdy/id388968174">iTunes</a>.<br>
